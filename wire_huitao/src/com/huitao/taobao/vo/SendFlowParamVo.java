package com.huitao.taobao.vo;

public class SendFlowParamVo {
        private String buyerNick;
        private String realNick;
        private String serial;
        private String flow;
        private String reason;
        private String always;
        private String userId;
        
		public String getBuyerNick() {
			return buyerNick;
		}
		public void setBuyerNick(String buyerNick) {
			this.buyerNick = buyerNick;
		}
		public String getRealNick() {
			return realNick;
		}
		public void setRealNick(String realNick) {
			this.realNick = realNick;
		}
		public String getSerial() {
			return serial;
		}
		public void setSerial(String serial) {
			this.serial = serial;
		}
		public String getFlow() {
			return flow;
		}
		public void setFlow(String flow) {
			this.flow = flow;
		}
		public String getReason() {
			return reason;
		}
		public void setReason(String reason) {
			this.reason = reason;
		}
		public String getAlways() {
			return always;
		}
		public void setAlways(String always) {
			this.always = always;
		}
		public String getUserId() {
			return userId;
		}
		public void setUserId(String userId) {
			this.userId = userId;
		}
        
}
